public class Override {
    public static void main(String[] args) {
        A ob = new A(); // Ajout du point-virgule pour terminer l'instruction
        System.out.println(ob.a); // Ajout du point-virgule pour terminer l'instruction
    }
}

// Classe A doit être définie
class A {
    int a = 10; // Exemple de champ "a" avec une valeur par défaut
}
